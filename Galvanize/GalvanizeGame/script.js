Actor = function(type,id,x,y,width,height,img,hp){
	var self = Entity(type,id,x,y,width,height,img);

	self.hp = hp;
	self.hpMax = hp;
	self.attackCounter = 0;
	self.aimAngle = 0;

	self.spriteAnimCounter = 0;

	self.draw = function(){
		canvas.save();
		var x = self.x - player.x;
		var y = self.y - player.y;

		x += WIDTH/2;
		y += HEIGHT/2;

		x -= self.width/2;
		y -= self.height/2;

		var frameWidth = self.img.width/2;
		var frameHeight = self.img.height/2;

		var aimAngle = self.aimAngle;
		if(aimAngle < 0)
			aimAngle = 360 + aimAngle;

		var directionMod = 3;	//draw right
		if(aimAngle >= 45 && aimAngle < 135)	//down
			directionMod = 2;
		else if(aimAngle >= 135 && aimAngle < 225)	//left
			directionMod = 1;
		else if(aimAngle >= 225 && aimAngle < 315)	//up
			directionMod = 0;


		canvas.drawImage(self.img,0,0,self.img.width,self.img.height,
		x,y,self.width,self.height
		);

		canvas.restore();
	}

	self.onDeath = function(){};

	self.performAttack = function(){
		if(self.attackCounter > 25){	//every 1 sec
			self.attackCounter = 0;
		}
	}

	self.performSpecialAttack = function(){
		if(self.attackCounter > 50){	//every 1 sec
			self.attackCounter = 0;

		}
	}


	return self;
}



Enemy = function(id,x,y,width,height,img,hp){
	var self = Actor('enemy',id,x,y,width,height,img,hp);
	var super_update = self.update;
	self.update = function(){
		super_update();

		var isColliding = player.testCollision(self);
		if(isColliding){
			player.hp = player.hp - 1;
		}
	}

	Enemy.list[id] = self;

	self.toRemove = false;


	self.updatePosition = function(){
		var oldX = self.x;
		var oldY = self.y;

		var diffX = player.x - self.x;
		var diffY = player.y - self.y;
		if(diffX > 0)
			self.x += 1;
		else
			self.x -= 1;

		if(diffY > 0)
			self.y += 1;
		else
			self.y -= 1;
		if(Maps.current.isPositionWall(self)){
			self.x = oldX;
			self.y = oldY;
		}
	}
}
